
# Declarar los tres conjuntos de países
paises_A = {'España', 'Francia', 'Italia', 'Alemania'}
paises_B = {'España', 'Italia', 'Portugal', 'Grecia'}
paises_C = {'Italia', 'Alemania', 'Polonia', 'Países Bajos'}

# Realizar la intersección de los tres conjuntos
paises_interseccion = paises_A.intersection(paises_B, paises_C)

# Mostrar el resultado
print(paises_interseccion)
